package com.riskified.models;

public class Custom {
    private String app_dom_id;
    
    public Custom() {
    }

    public Custom(String app_dom_id) {
        this.app_dom_id = app_dom_id;
    }

    public String getAppDomId() {
        return app_dom_id;
    }

    public void setAppDomId(String app_dom_id) {
        this.app_dom_id = app_dom_id;
    }
}
